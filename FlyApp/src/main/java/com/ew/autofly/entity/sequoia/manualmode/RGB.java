package com.ew.autofly.entity.sequoia.manualmode;



public class RGB {

    private String mode;

    private int exp_us;

    private int iso;

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public int getExp_us() {
        return exp_us;
    }

    public void setExp_us(int exp_us) {
        this.exp_us = exp_us;
    }

    public int getIso() {
        return iso;
    }

    public void setIso(int iso) {
        this.iso = iso;
    }
}
