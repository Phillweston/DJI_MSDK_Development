package com.ew.autofly.entity.sequoia;



public class Version {

    private String request;

    private String version;

    private String serial_number;

    private String sunshine_serial_number;

    public String getRequest() {
        return request;
    }

    public void setRequest(String request) {
        this.request = request;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getSerial_number() {
        return serial_number;
    }

    public void setSerial_number(String serial_number) {
        this.serial_number = serial_number;
    }

    public String getSunshine_serial_number() {
        return sunshine_serial_number;
    }

    public void setSunshine_serial_number(String sunshine_serial_number) {
        this.sunshine_serial_number = sunshine_serial_number;
    }
}
